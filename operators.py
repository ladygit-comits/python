#arithmetic operators
num1 = 34
num2 = 10

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #modulus - returns the remainder

#comparison operators
print(5 == 5) #returns True
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #Equal to
print(num1 != num2) # Not equal to

#Assignment operators
number = 12

print(number)

number += 2
print(number)

b = 56
b -= 11
print(b)

#logic operators - and or not
print(34 < 10 and 89 > 5)
print(34 < 10 or 89 > 5)
print (not(34 < 10 or 89 > 5))